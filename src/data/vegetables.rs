    fn test_get_vegetable_by_id_found() {
        let result = get_vegetable_by_id("tomate");
        assert!(result.is_some());
        assert_eq!(result.unwrap().name, "Tomate");
    }

    #[test]
    fn test_get_vegetable_by_id_not_found() {
        let result = get_vegetable_by_id("legume-inexistant");
        assert!(result.is_none());
    }

